# -*- coding: utf-8 -*-
"""
محرك سيناريوهات الموازنة العراقية — إصدار مُطوّر (2026)
=========================================================
يضيف هذا الإصدار تحليلاً لسعر الصرف إلى جانب أسعار النفط:

1) تحليل تخفيض سعر الصرف:
   تأثير رفع سعر الصرف (دينار/دولار) على الإيرادات النفطية المحوّلة
   إلى الدينار وعلى الرصيد والاحتياطي.

2) الفجوة بين السعر الموازي والرسمي:
   حساب الفجوة ونسبتها، وأثر سدّها كليًا أو جزئيًا على المالية العامة.

3) الحساسية المزدوجة (نفط × صرف):
   مصفوفة الرصيد عند كل تركيبة من سعر النفط وسعر الصرف.

4) أثر التضخم والاستيراد:
   تقدير تقريبي للتضخم المستورد وكلفة فاتورة الاستيراد الحكومية
   نتيجة التخفيض (تمرير سعر الصرف إلى الأسعار).

ملاحظة منهجية: قاعدة الاحتياطي كما في الأصل — العجز يُموَّل جزئيًا
من المركزي (يخفض الاحتياطي)، والفائض يُخصَّص جزء منه للاحتياطي.
"""

from dataclasses import dataclass, field
from typing import Dict, List, Tuple
import os
import pandas as pd


# ----------------------------------------------------------------------
# المدخلات
# ----------------------------------------------------------------------
@dataclass
class مدخلات:
    # النفط والصادرات
    اسعار_النفط: List[float]
    حجم_الصادرات_مليون_برميل_يوم: float
    الاستهلاك_المحلي_مليون_برميل_يوم: float
    حصة_الحكومة_من_الصادرات: float
    ايام_السنة: int

    # سعر الصرف
    سعر_الصرف_الرسمي: float           # السعر الرسمي الحالي (دينار/دولار)
    سعر_الصرف_الموازي: float          # السعر الموازي في السوق
    اسعار_الصرف_سيناريو: List[float]  # قائمة أسعار صرف لتحليل التخفيض

    # المالية العامة (مليار دينار)
    ايرادات_غير_نفطية_مليار: float
    اجمالي_النفقات_مليار: float
    النفقات_الجارية_مليار: float
    استخدام_الاجمالي: bool

    # النقد والاحتياطي (مليار دينار)
    الاحتياطيات_الاجنبية_مليار: float
    النقد_القاعدي_مليار: float
    نسبة_تمويل_المركزي: float
    نسبة_الفائض_للاحتياطي: float

    # التضخم والاستيراد
    واردات_سنوية_مليار_دولار: float = 60.0   # تقدير فاتورة الاستيراد السنوية
    حصة_المستورد_من_السلة: float = 0.40       # وزن السلع المستوردة في سلة المستهلك
    معامل_تمرير_التضخم: float = 0.50          # نسبة تمرير تغير الصرف إلى الأسعار

    اسم_السيناريو: str = "السيناريو الافتراضي"


# ----------------------------------------------------------------------
# أدوات مساعدة
# ----------------------------------------------------------------------
def _excel_engine() -> str:
    try:
        import xlsxwriter  # noqa: F401
        return "xlsxwriter"
    except Exception:
        return "openpyxl"


def صافي_براميل_التصدير(p: "مدخلات") -> float:
    """صافي الصادرات الحكومية (مليون برميل/يوم)."""
    return max(
        p.حجم_الصادرات_مليون_برميل_يوم - p.الاستهلاك_المحلي_مليون_برميل_يوم,
        0.0,
    ) * p.حصة_الحكومة_من_الصادرات


# ----------------------------------------------------------------------
# الحسابات الأساسية
# ----------------------------------------------------------------------
def حساب_الايرادات_النفطية(سعر_النفط: float, سعر_الصرف: float, p: "مدخلات") -> float:
    """الإيرادات النفطية بالمليار دينار عند سعر نفط وسعر صرف معيّنين."""
    صادرات = صافي_براميل_التصدير(p)
    براميل_سنوياً = صادرات * 1_000_000 * p.ايام_السنة
    بالدولار = سعر_النفط * براميل_سنوياً
    بالدينار = بالدولار * سعر_الصرف
    return بالدينار / 1e9  # مليار دينار


def حساب_الموازنة(سعر_النفط: float, سعر_الصرف: float, p: "مدخلات") -> Dict[str, float]:
    """صف موازنة كامل عند سعر نفط وسعر صرف."""
    ايراد_نفطي = حساب_الايرادات_النفطية(سعر_النفط, سعر_الصرف, p)
    اجمالي_الايرادات = ايراد_نفطي + p.ايرادات_غير_نفطية_مليار
    الانفاق = p.اجمالي_النفقات_مليار if p.استخدام_الاجمالي else p.النفقات_الجارية_مليار
    الرصيد = اجمالي_الايرادات - الانفاق  # (+) فائض، (-) عجز

    تمويل_المركزي = -الرصيد * p.نسبة_تمويل_المركزي if الرصيد < 0 else 0.0
    تخصيص_فائض = (الرصيد * p.نسبة_الفائض_للاحتياطي) if الرصيد > 0 else 0.0

    صافي_تغير_الاحتياطي = تخصيص_فائض - تمويل_المركزي
    الاحتياطي_بعد = p.الاحتياطيات_الاجنبية_مليار + صافي_تغير_الاحتياطي
    الفجوة_النقدية = الاحتياطي_بعد - p.النقد_القاعدي_مليار

    return {
        "سعر النفط (دولار)": سعر_النفط,
        "سعر الصرف (دينار/دولار)": سعر_الصرف,
        "الإيرادات النفطية (مليار دينار)": ايراد_نفطي,
        "الإيرادات غير النفطية (مليار دينار)": p.ايرادات_غير_نفطية_مليار,
        "إجمالي الإيرادات (مليار دينار)": اجمالي_الايرادات,
        "النفقات (مليار دينار)": الانفاق,
        "الرصيد (مليار دينار)": الرصيد,
        "تمويل المركزي (مليار دينار)": تمويل_المركزي,
        "تخصيص الفائض للاحتياطي (مليار دينار)": تخصيص_فائض,
        "صافي تغير الاحتياطي (مليار دينار)": صافي_تغير_الاحتياطي,
        "الاحتياطي بعد (مليار دينار)": الاحتياطي_بعد,
        "النقد القاعدي (مليار دينار)": p.النقد_القاعدي_مليار,
        "الفجوة النقدية (مليار دينار)": الفجوة_النقدية,
    }


# ----------------------------------------------------------------------
# جداول السيناريوهات
# ----------------------------------------------------------------------
def جدول_حسب_سعر_النفط(p: "مدخلات", سعر_الصرف: float) -> pd.DataFrame:
    """جدول الموازنة عبر أسعار النفط عند سعر صرف ثابت."""
    rows = [حساب_الموازنة(x, سعر_الصرف, p) for x in p.اسعار_النفط]
    return (
        pd.DataFrame(rows)
        .sort_values("سعر النفط (دولار)")
        .reset_index(drop=True)
    )


def جدول_حسب_سعر_الصرف(p: "مدخلات", سعر_النفط: float) -> pd.DataFrame:
    """جدول الموازنة عبر أسعار الصرف عند سعر نفط ثابت (تحليل التخفيض)."""
    rows = [حساب_الموازنة(سعر_النفط, fx, p) for fx in p.اسعار_الصرف_سيناريو]
    df = (
        pd.DataFrame(rows)
        .sort_values("سعر الصرف (دينار/دولار)")
        .reset_index(drop=True)
    )
    # نسبة التغير في الرصيد مقارنة بالسعر الرسمي الأساس
    اساس = حساب_الموازنة(سعر_النفط, p.سعر_الصرف_الرسمي, p)["الرصيد (مليار دينار)"]
    df["تحسن الرصيد عن الأساس (مليار دينار)"] = df["الرصيد (مليار دينار)"] - اساس
    return df


def مصفوفة_الحساسية(p: "مدخلات") -> pd.DataFrame:
    """مصفوفة الرصيد (مليار دينار): الصفوف أسعار صرف، الأعمدة أسعار نفط."""
    اسعار_صرف = sorted(set(p.اسعار_الصرف_سيناريو))
    اسعار_نفط = sorted(set(p.اسعار_النفط))
    data = {}
    for س_نفط in اسعار_نفط:
        data[س_نفط] = [
            حساب_الموازنة(س_نفط, fx, p)["الرصيد (مليار دينار)"]
            for fx in اسعار_صرف
        ]
    mat = pd.DataFrame(data, index=اسعار_صرف)
    mat.index.name = "سعر الصرف"
    mat.columns.name = "سعر النفط"
    return mat


# ----------------------------------------------------------------------
# الفجوة بين السعر الموازي والرسمي
# ----------------------------------------------------------------------
def تحليل_الفجوة(p: "مدخلات", نسبة_السد: float, سعر_النفط: float) -> Dict[str, float]:
    """
    تحليل سدّ الفجوة بين السعر الموازي والرسمي.
    نسبة_السد: كم من الفجوة يُغلق عبر تعديل السعر الرسمي (0 إلى 1).
    """
    رسمي = p.سعر_الصرف_الرسمي
    موازي = p.سعر_الصرف_الموازي
    الفجوة = موازي - رسمي
    نسبة_الفجوة = (الفجوة / رسمي) if رسمي else 0.0

    # السعر الرسمي الجديد بعد سدّ نسبة من الفجوة
    رسمي_جديد = رسمي + الفجوة * نسبة_السد

    رصيد_قبل = حساب_الموازنة(سعر_النفط, رسمي, p)["الرصيد (مليار دينار)"]
    رصيد_بعد = حساب_الموازنة(سعر_النفط, رسمي_جديد, p)["الرصيد (مليار دينار)"]

    تضخم = تقدير_التضخم(p, رسمي, رسمي_جديد)

    return {
        "السعر الرسمي الحالي": رسمي,
        "السعر الموازي": موازي,
        "الفجوة (دينار)": الفجوة,
        "نسبة الفجوة %": نسبة_الفجوة * 100.0,
        "نسبة السد %": نسبة_السد * 100.0,
        "السعر الرسمي الجديد": رسمي_جديد,
        "الرصيد قبل (مليار دينار)": رصيد_قبل,
        "الرصيد بعد (مليار دينار)": رصيد_بعد,
        "تحسن الرصيد (مليار دينار)": رصيد_بعد - رصيد_قبل,
        "التضخم المستورد التقديري %": تضخم["التضخم المستورد التقديري %"],
        "زيادة فاتورة الاستيراد (مليار دينار)": تضخم["زيادة فاتورة الاستيراد (مليار دينار)"],
    }


# ----------------------------------------------------------------------
# أثر التضخم والاستيراد
# ----------------------------------------------------------------------
def تقدير_التضخم(p: "مدخلات", سعر_قديم: float, سعر_جديد: float) -> Dict[str, float]:
    """
    تقدير تقريبي لأثر تغيّر سعر الصرف على التضخم وفاتورة الاستيراد.
    التضخم المستورد ≈ حصة المستورد × معامل التمرير × نسبة تغير الصرف.
    """
    if سعر_قديم <= 0:
        return {
            "نسبة تغير الصرف %": 0.0,
            "التضخم المستورد التقديري %": 0.0,
            "زيادة فاتورة الاستيراد (مليار دينار)": 0.0,
            "فاتورة الاستيراد قبل (مليار دينار)": 0.0,
            "فاتورة الاستيراد بعد (مليار دينار)": 0.0,
        }

    نسبة_التغير = (سعر_جديد - سعر_قديم) / سعر_قديم
    تضخم_مستورد = (
        p.حصة_المستورد_من_السلة * p.معامل_تمرير_التضخم * نسبة_التغير
    )

    # فاتورة الاستيراد بالمليار دينار = الواردات (مليار دولار) × سعر الصرف
    فاتورة_قبل = p.واردات_سنوية_مليار_دولار * سعر_قديم
    فاتورة_بعد = p.واردات_سنوية_مليار_دولار * سعر_جديد

    return {
        "نسبة تغير الصرف %": نسبة_التغير * 100.0,
        "التضخم المستورد التقديري %": تضخم_مستورد * 100.0,
        "زيادة فاتورة الاستيراد (مليار دينار)": فاتورة_بعد - فاتورة_قبل,
        "فاتورة الاستيراد قبل (مليار دينار)": فاتورة_قبل,
        "فاتورة الاستيراد بعد (مليار دينار)": فاتورة_بعد,
    }


# ----------------------------------------------------------------------
# الملخص العام
# ----------------------------------------------------------------------
def ملخص_السيناريو(p: "مدخلات", سعر_الصرف: float) -> Dict[str, float]:
    df = جدول_حسب_سعر_النفط(p, سعر_الصرف)
    اسوأ = df.loc[df["الرصيد (مليار دينار)"].idxmin()]
    افضل = df.loc[df["الرصيد (مليار دينار)"].idxmax()]
    عند_اقل_سعر = df.loc[df["سعر النفط (دولار)"].idxmin()]

    # سعر النفط الذي يحقق التعادل (الرصيد = صفر) عند سعر الصرف الحالي
    صادرات = صافي_براميل_التصدير(p)
    براميل_سنوياً = صادرات * 1_000_000 * p.ايام_السنة
    الانفاق = p.اجمالي_النفقات_مليار if p.استخدام_الاجمالي else p.النفقات_الجارية_مليار
    مطلوب_من_النفط_مليار = الانفاق - p.ايرادات_غير_نفطية_مليار
    سعر_التعادل = None
    if براميل_سنوياً > 0 and سعر_الصرف > 0:
        سعر_التعادل = (مطلوب_من_النفط_مليار * 1e9) / (براميل_سنوياً * سعر_الصرف)

    return {
        "اسم السيناريو": p.اسم_السيناريو,
        "سعر الصرف المستخدم": سعر_الصرف,
        "أقل سعر نفط": float(df["سعر النفط (دولار)"].min()),
        "أعلى سعر نفط": float(df["سعر النفط (دولار)"].max()),
        "أكبر عجز (ترليون)": float(اسوأ["الرصيد (مليار دينار)"] / 1000.0),
        "أكبر فائض (ترليون)": float(افضل["الرصيد (مليار دينار)"] / 1000.0),
        "الاحتياطي عند أقل سعر (ترليون)": float(عند_اقل_سعر["الاحتياطي بعد (مليار دينار)"] / 1000.0),
        "الفجوة النقدية عند أقل سعر (ترليون)": float(عند_اقل_سعر["الفجوة النقدية (مليار دينار)"] / 1000.0),
        "سعر نفط التعادل (دولار)": (float(سعر_التعادل) if سعر_التعادل is not None else None),
    }


# توافق رجعي مع الإصدار القديم
def تشغيل_السيناريو(p: "مدخلات") -> Tuple[pd.DataFrame, Dict[str, float]]:
    سعر_الصرف = p.سعر_الصرف_الرسمي
    df = جدول_حسب_سعر_النفط(p, سعر_الصرف)
    return df, ملخص_السيناريو(p, سعر_الصرف)


# ----------------------------------------------------------------------
# التصدير
# ----------------------------------------------------------------------
def حفظ_النتائج(جداول: Dict[str, pd.DataFrame], path: str):
    """يحفظ عدة جداول في ملف Excel واحد (كل جدول في ورقة)."""
    os.makedirs(os.path.dirname(path) or ".", exist_ok=True)
    with pd.ExcelWriter(path, engine=_excel_engine()) as writer:
        for اسم, df in جداول.items():
            # أسماء أوراق Excel ≤ 31 حرفاً
            df.to_excel(writer, sheet_name=str(اسم)[:31], index=(df.index.name is not None))
    return path
